package com.example.afcs.model;

public class StopEntity {
	
	private String stopId;
	private String stopName;
	private String distanceFromSrc;
	private String stopLat;
	private String stopLong;
	public String getStopId() {
		return stopId;
	}
	public void setStopId(String stopId) {
		this.stopId = stopId;
	}
	public String getStopName() {
		return stopName;
	}
	public void setStopName(String stopName) {
		this.stopName = stopName;
	}
	public String getDistanceFromSrc() {
		return distanceFromSrc;
	}
	public void setDistanceFromSrc(String distanceFromSrc) {
		this.distanceFromSrc = distanceFromSrc;
	}
	public String getStopLat() {
		return stopLat;
	}
	public void setStopLat(String stopLat) {
		this.stopLat = stopLat;
	}
	public String getStopLong() {
		return stopLong;
	}
	public void setStopLong(String stopLong) {
		this.stopLong = stopLong;
	}
	
	
}
